package Model.Player;

/**
 * The {@code WorkerColor} enum defines the possible colors that a worker can have in the game.
 * The available colors are Purple, Orange, and Red, representing different worker colors for each player.
 * <p>
 * This enum helps to distinguish between workers and provides a way to assign colors to workers
 * in the Santorini game.
 * </p>
 */
public enum WorkerColor {
    /**
     * Represents the purple worker color.
     */
    PURPLE,
    /**
     * Represents the orange worker color.
     */
    ORANGE,
    /**
     * Represents the red worker color.
     */
    RED;
}
